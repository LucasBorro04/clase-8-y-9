package Switch_case;

public class SwitchCase {
    public static void main(String args[]) {
        char x = 'a';
        switch (x) {
            case 'a':
            {
                System.out.println("Esta es la operacion 0");
                break;

            }
            case'b':
            {
                System.out.println("Esta es la opcion 1");
                break;
            }
            default:
            {
                System.out.println("Esta opcion es la opcion por defecto");
            }
        }

    }}

